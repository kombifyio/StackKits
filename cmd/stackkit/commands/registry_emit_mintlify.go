var (
	registryEmitMintlifySrcInput string
	registryEmitMintlifyOutDir   string
)

var registryEmitMintlifyCmd = &cobra.Command{
	Use:   "emit-mintlify",
	Short: "Render Mintlify MDX tool documentation from the registry snapshot",
	Long: `Render guides/stackkits/services/<slug>.mdx for every tool in the snapshot
that carries published content (tool.content != null). Tools without content
are skipped — their handwritten MDX is left untouched.

Reads the snapshot JSON (default: internal/registry/data/registry_snapshot.json)
and renders deterministically. The header carries the content_hash so drift is
detectable. CI regenerates from the committed snapshot and diffs.`,
	RunE: runRegistryEmitMintlify,
}

func init() {
	registryEmitMintlifyCmd.Flags().StringVar(&registryEmitMintlifySrcInput, "input", defaultSnapshotPath(), "Snapshot JSON to render from")
	registryEmitMintlifyCmd.Flags().StringVar(&registryEmitMintlifyOutDir, "output-dir", filepath.Join("guides", "stackkits", "services"), "Output directory for generated MDX files")
	registryCmd.AddCommand(registryEmitMintlifyCmd)
}

func runRegistryEmitMintlify(_ *cobra.Command, _ []string) error {
	raw, err := os.ReadFile(registryEmitMintlifySrcInput) // #nosec G304
	if err != nil {
		return fmt.Errorf("read snapshot %s: %w", registryEmitMintlifySrcInput, err)
	}
	var snap registry.Snapshot
	if err := json.Unmarshal(raw, &snap); err != nil {
		return fmt.Errorf("decode snapshot %s: %w", registryEmitMintlifySrcInput, err)
	}
	if snap.SchemaVersion != registry.SnapshotVersion {
		return fmt.Errorf("snapshot schema version mismatch: got %d, expected %d",
			snap.SchemaVersion, registry.SnapshotVersion)
	}

	if err := os.MkdirAll(registryEmitMintlifyOutDir, 0o755); err != nil {
		return fmt.Errorf("mkdir %s: %w", registryEmitMintlifyOutDir, err)
	}

	sorted := append([]registry.Tool(nil), snap.Tools...)
	sort.Slice(sorted, func(i, j int) bool { return sorted[i].Slug < sorted[j].Slug })

	generated := 0
	for _, tool := range sorted {
		if tool.Content == nil {
			continue
		}
		mdx, err := renderToolDocMDX(tool, snap)
		if err != nil {
			return fmt.Errorf("render %s: %w", tool.Slug, err)
		}
		outPath := filepath.Join(registryEmitMintlifyOutDir, tool.Slug+".mdx")
		if err := os.WriteFile(outPath, []byte(mdx), 0o644); err != nil { // #nosec G306
			return fmt.Errorf("write %s: %w", outPath, err)
		}
		generated++
	}

	printSuccess("emit-mintlify: wrote %d tool docs to %s", generated, registryEmitMintlifyOutDir)
	printInfo("snapshot_source=%s content_hash=%s tools_total=%d tools_with_content=%d",
		snap.Source, orNone(snap.ContentHash), len(snap.Tools), generated)
	return nil
}

