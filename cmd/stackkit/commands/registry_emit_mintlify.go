type toolDocData struct {
	Tool             registry.Tool
	Sections         registry.ToolContentSections
	SnapshotHash     string
	Description      string
	Icon             string
	ServiceSlug      string
	HasAlternatives  bool
	AlternativesNote string
}

func renderToolDocMDX(tool registry.Tool, snap registry.Snapshot) (string, error) {
	sec := tool.Content.Sections

	description := sec.Overview
	if len(description) > 120 {
		if idx := strings.Index(description[:120], "."); idx > 30 {
			description = description[:idx+1]
		} else {
			description = description[:120] + "..."
		}
	}

	icon := toolIcon(tool.Category)
	alternatives := findAlternatives(tool, snap)

	data := toolDocData{
		Tool:             tool,
		Sections:         sec,
		SnapshotHash:     snap.ContentHash,
		Description:      description,
		Icon:             icon,
		ServiceSlug:      tool.Slug,
		HasAlternatives:  len(alternatives) > 0,
		AlternativesNote: formatAlternativesNote(tool, alternatives),
	}

	var buf strings.Builder
	if err := toolDocTemplate.Execute(&buf, data); err != nil {
		return "", err
	}
	return buf.String(), nil
}

func toolIcon(category string) string {
	switch category {
	case "ingress", "reverse-proxy":
		return "globe"
	case "auth":
		return "lock"
	case "dns":
		return "shield"
	case "observability":
		return "chart-line"
	case "dev-platform":
		return "rocket"
	case "media":
		return "film"
	case "storage":
		return "folder"
	case "secrets":
		return "key"
	case "database":
		return "database"
	case "webserver":
		return "server"
	case "automation":
		return "robot"
	default:
		return "cube"
	}
}

func findAlternatives(tool registry.Tool, snap registry.Snapshot) []string {
	for _, sg := range snap.ServiceGroups {
		if sg.DefaultModuleSlug == tool.Slug {
			for _, kit := range snap.StackKits {
				for _, sel := range kit.ServiceSelections {
					if sel.ServiceGroupSlug == sg.Slug && len(sel.AlternativeModuleSlugs) > 0 {
						return sel.AlternativeModuleSlugs
					}
				}
			}
		}
	}
	for _, kit := range snap.StackKits {
		for _, sel := range kit.ServiceSelections {
			if slices.Contains(sel.AlternativeModuleSlugs, tool.Slug) {
				others := []string{sel.SelectedModuleSlug}
				for _, a := range sel.AlternativeModuleSlugs {
					if a != tool.Slug {
						others = append(others, a)
					}
				}
				return others
			}
		}
	}
	return nil
}

func formatAlternativesNote(tool registry.Tool, alternatives []string) string {
	if len(alternatives) == 0 {
		return ""
	}
	links := make([]string, 0, len(alternatives))
	for _, alt := range alternatives {
		links = append(links, fmt.Sprintf("[%s](/guides/stackkits/services/%s)", alt, alt))
	}
	return fmt.Sprintf("%s and %s are interchangeable — pick one. See [tool alternatives](/stackkits/reference/tool-alternatives).",
		tool.DisplayName, strings.Join(links, ", "))
}
